// City grouping helper — returns cities (with >=1 clinic) for this state.
export function getCaliforniaDexaClinicsByCity(citySlug: string): DexaClinic[] {
  return californiaDexaClinics.filter((c) => c.citySlug === citySlug);
}

export function getCaliforniaDexaCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const map = new Map<string, { city: string; citySlug: string; count: number }>();
  for (const clinic of californiaDexaClinics) {
    const existing = map.get(clinic.citySlug);
    if (existing) {
      existing.count += 1;
    } else {
      map.set(clinic.citySlug, { city: clinic.city, citySlug: clinic.citySlug, count: 1 });
    }
  }
  return Array.from(map.values());
}
