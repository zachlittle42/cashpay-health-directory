// Helper function to get clinics by city
export function getTexasClinicsByCity(citySlug: string): HormoneClinic[] {
  return texasHormoneClinics.filter(clinic => clinic.citySlug === citySlug);
}

// Get all Texas cities with clinics
export function getTexasCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, { city: string; count: number }>();
  texasHormoneClinics.forEach(clinic => {
    const existing = cities.get(clinic.citySlug);
    if (existing) {
      existing.count++;
    } else {
      cities.set(clinic.citySlug, { city: clinic.city, count: 1 });
    }
  });
  return Array.from(cities.entries()).map(([citySlug, data]) => ({
    citySlug,
    city: data.city,
    count: data.count,
  }));
}
