// Helper functions
export function getFloridaWeightLossClinicsByCity(citySlug: string): WeightLossClinic[] {
  return floridaWeightLossClinics.filter(clinic => clinic.citySlug === citySlug);
}

export function getFloridaWeightLossCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, { city: string; count: number }>();
  floridaWeightLossClinics.forEach(clinic => {
    const existing = cities.get(clinic.citySlug);
    if (existing) {
      existing.count++;
    } else {
      cities.set(clinic.citySlug, { city: clinic.city, count: 1 });
    }
  });
  return Array.from(cities.entries()).map(([citySlug, data]) => ({
    citySlug,
    city: data.city,
    count: data.count,
  }));
}
