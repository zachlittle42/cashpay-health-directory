// Helper functions
export function getMexicoClinicsByCity(citySlug: string): StemCellClinic[] {
  const cityMap: Record<string, string> = {
    'tijuana': 'Tijuana',
    'cancun': 'Cancun',
    'puerto-vallarta': 'Puerto Vallarta',
    'los-cabos': 'Los Cabos',
    'mexico-city': 'Mexico City',
  };
  const cityName = cityMap[citySlug];
  return mexicoStemCellClinics.filter(clinic => clinic.city === cityName);
}

export function getMexicoCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, number>();
  const citySlugMap: Record<string, string> = {
    'Tijuana': 'tijuana',
    'Cancun': 'cancun',
    'Puerto Vallarta': 'puerto-vallarta',
    'Los Cabos': 'los-cabos',
    'Mexico City': 'mexico-city',
  };

  mexicoStemCellClinics.forEach(clinic => {
    cities.set(clinic.city, (cities.get(clinic.city) || 0) + 1);
  });

  return Array.from(cities.entries()).map(([city, count]) => ({
    city,
    citySlug: citySlugMap[city] || city.toLowerCase().replace(/\s+/g, '-'),
    count,
  }));
}
