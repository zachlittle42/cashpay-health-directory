// Helper functions
export function getTexasWeightLossClinicsByCity(citySlug: string): WeightLossClinic[] {
  return texasWeightLossClinics.filter(clinic => clinic.citySlug === citySlug);
}

export function getTexasWeightLossCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, { city: string; count: number }>();
  texasWeightLossClinics.forEach(clinic => {
    const existing = cities.get(clinic.citySlug);
    if (existing) {
      existing.count++;
    } else {
      cities.set(clinic.citySlug, { city: clinic.city, count: 1 });
    }
  });
  return Array.from(cities.entries()).map(([citySlug, data]) => ({
    citySlug,
    city: data.city,
    count: data.count,
  }));
}
