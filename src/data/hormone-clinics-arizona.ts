// Helper function to get clinics by city
export function getArizonaClinicsByCity(citySlug: string): HormoneClinic[] {
  return arizonaHormoneClinics.filter(clinic => clinic.citySlug === citySlug);
}

// Get all Arizona cities with clinics
export function getArizonaCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, { city: string; count: number }>();
  arizonaHormoneClinics.forEach(clinic => {
    const existing = cities.get(clinic.citySlug);
    if (existing) {
      existing.count++;
    } else {
      cities.set(clinic.citySlug, { city: clinic.city, count: 1 });
    }
  });
  return Array.from(cities.entries()).map(([citySlug, data]) => ({
    citySlug,
    city: data.city,
    count: data.count,
  }));
}
