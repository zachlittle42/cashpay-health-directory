// Helper functions
export function getArizonaWeightLossClinicsByCity(citySlug: string): WeightLossClinic[] {
  return arizonaWeightLossClinics.filter(clinic => clinic.citySlug === citySlug);
}

export function getArizonaWeightLossCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, { city: string; count: number }>();
  arizonaWeightLossClinics.forEach(clinic => {
    const existing = cities.get(clinic.citySlug);
    if (existing) {
      existing.count++;
    } else {
      cities.set(clinic.citySlug, { city: clinic.city, count: 1 });
    }
  });
  return Array.from(cities.entries()).map(([citySlug, data]) => ({
    citySlug,
    city: data.city,
    count: data.count,
  }));
}
