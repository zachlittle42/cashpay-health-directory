// Helper function to get clinics by city
export function getFloridaClinicsByCity(citySlug: string): HormoneClinic[] {
  return floridaHormoneClinics.filter(clinic => clinic.citySlug === citySlug);
}

// Get all Florida cities with clinics
export function getFloridaCitiesWithClinics(): { city: string; citySlug: string; count: number }[] {
  const cities = new Map<string, { city: string; count: number }>();
  floridaHormoneClinics.forEach(clinic => {
    const existing = cities.get(clinic.citySlug);
    if (existing) {
      existing.count++;
    } else {
      cities.set(clinic.citySlug, { city: clinic.city, count: 1 });
    }
  });
  return Array.from(cities.entries()).map(([citySlug, data]) => ({
    citySlug,
    city: data.city,
    count: data.count,
  }));
}
